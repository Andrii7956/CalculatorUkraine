import math

def додавання(x, y):
    return x + y

def віднімання(x, y):
    return x - y

def множення(x, y):
    return x * y

def ділення(x, y):
    return x / y

def піднесення_до_степеня(x, y):
    return x ** y

def квадратний_корінь(x):
    return math.sqrt(x)

def факторіал(x):
    if x == 0:
        return 1
    else:
        return x * факторіал(x - 1)

def залишок_від_ділення(x, y):
    return x % y

def логарифм(x, base):
    return math.log(x, base)

def округлення(x):
    return round(x)

def синус(x):
    return math.sin(x)

def косинус(x):
    return math.cos(x)

def тангенс(x):
    return math.tan(x)

def натуральний_логарифм(x):
    return math.log(x)

def десятковий_логарифм(x):
    return math.log10(x)

def обернене_значення(x):
    return 1 / x

def факторіальна_послідовність(x):
    послідовність = []
    for i in range(1, x + 1):
        послідовність.append(факторіал(i))
    return послідовність

def модуль(x):
    return abs(x)

def округлення_вниз(x):
    return math.floor(x)

def округлення_вгору(x):
    return math.ceil(x)

